def add(x,y):
    result = x + y
    return f" The Addition of {x} and {y} = {result}"
print(add(2, 3))

def subtract(x,y):
    result = x - y
    return f" The Subtraction of {x} and {y} = {result}"
print(subtract(6, 2))

def divide(x, y):
    result = x / y
    return f" The Division of {x} and {y} = {result}"
print(divide(10, 2))

def multiply(x,y):
    result = x * y
    return f"The Multiplication of {x} and {y} = {result}"
print(multiply(5,2))

def mod(x, y):
    result = x % y
    return f"The Remainder of Dividing of {x} and {y} = {result}"
print(mod(10, 5))

